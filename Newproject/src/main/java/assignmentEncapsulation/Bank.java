package assignmentEncapsulation;

public class Bank {
	private long  pin;
	private String name;
	
	public void setname(String name)
	{
		
		this.name = name;
	}
	public String getname()
	{
		
		return name;
	}
	public void setpin(long pin)
	{
		
		this.pin = pin;
	}
	public long  getpin()
	{
		
		return pin;
	}

}
